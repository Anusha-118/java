class Method10 {
  public static void main(String args[]) {
    System.out.println(anu(20, 15)); // print the returned value
  }

  public static String anu(int a, int b) { // 'String' must be capitalized
    if (a >= 10 && b >= 10) {
      return "mani";
    } else {
      return "anu";
    }
  }
}
